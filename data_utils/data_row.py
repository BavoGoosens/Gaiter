

class DataRow:
    def __init__(self, t, x, y, z):
        self.time = t
        self.x = x
        self.y = y
        self.z = z

    def get_time(self):
        return self.time

    def get_x(self):
        return self.x

    def get_y(self):
        return self.y

    def get_z(self):
        return self.z








